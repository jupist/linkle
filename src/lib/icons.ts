/** produces a 2-char monogram from an institution or company name */
export function entryicon(text: string): string {
	const cleaned = text.replace(/^(The |International )/i, '');
	const words = cleaned.split(/\s+/).filter((w) => /^[A-Z]/.test(w));
	if (words.length >= 2) return (words[0][0] + words[1][0]).toUpperCase();
	return cleaned.slice(0, 2).toUpperCase();
}

/** produces initials from a person's name */
export function initials(name: string): string {
	return name
		.replace(/^Prof\.\s*/i, '')
		.split(' ')
		.map((s) => s[0])
		.slice(0, 2)
		.join('')
		.toUpperCase();
}
